//===== Flash LED on pin 13 =====================================
void flash13() {

    pinMode(13, OUTPUT); // LED
    
//Flash the led a few times to show we are awake.
    digitalWrite(13, HIGH);
    delay(200);
    digitalWrite(13, LOW);
    delay(200);
    digitalWrite(13, HIGH);
    delay(200);
    digitalWrite(13, LOW);
    delay(200);
    digitalWrite(13, HIGH);
    delay(200);
    digitalWrite(13, LOW);

}
